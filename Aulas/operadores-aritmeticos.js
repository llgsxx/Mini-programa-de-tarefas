// Operadores Aritméticos em Node.js (JavaScript)
// ----------------------------------------------
// Operadores aritméticos são usados para realizar operações matemáticas entre valores ou variáveis.

// Principais operadores:
/*
    +  Adição         (soma dois valores)
    -  Subtração      (subtrai um valor do outro)
    *  Multiplicação  (multiplica dois valores)
    /  Divisão        (divide um valor pelo outro)
    %  Módulo         (resto da divisão)
    ** Exponenciação  (potência)
*/

// Exemplos práticos:
let a = 10;
let b = 3;

let soma = a + b;          // 13
let subtracao = a - b;     // 7
let multiplicacao = a * b; // 30
let divisao = a / b;       // 3.333...
let resto = a % b;         // 1 (resto da divisão de 10 por 3)
let potencia = a ** b;     // 1000 (10 elevado à 3ª potência)

// Você pode usar os operadores para fazer contas em variáveis ou diretamente em números.
console.log("Soma:", soma);
console.log("Subtração:", subtracao);
console.log("Multiplicação:", multiplicacao);
console.log("Divisão:", divisao);
console.log("Resto:", resto);
console.log("Potência:", potencia);

// Dica: operadores aritméticos funcionam em qualquer parte do código que precise de cálculos!
